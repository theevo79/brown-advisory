class CPIService:
    @staticmethod
    def get_cpi_for_year(country: str, year: int) -> Optional[float]:
        if country not in CPI_DATA:
            return None
        return CPI_DATA[country].get(year)

    @staticmethod
    def calculate_inflation_adjustments(country: str, years: List[int], base_year: Optional[int] = None) -> List[Optional[float]]:
        if not years:
            return []
        if base_year is None:
            base_year = max(years)
        base_cpi = CPIService.get_cpi_for_year(country, base_year)
        if base_cpi is None:
            return [None] * len(years)
        adjustments = []
        for year in years:
            year_cpi = CPIService.get_cpi_for_year(country, year)
            if year_cpi is None or year_cpi <= 0:
                adjustments.append(None)
            else:
                adjustments.append(base_cpi / year_cpi)
        return adjustments

    @staticmethod
    def has_cpi_data(country: str) -> bool:
        return country in CPI_DATA
